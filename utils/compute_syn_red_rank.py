"""
Compute Synergy-Redundancy Rank for GSM8K time series data.

This script processes 3 CSV files (al, ml, al+ml from GSM8K),
computes PhiID-based synergy and redundancy for time series data,
and generates Syn-Red Rank scores.

Input: Time series CSV files (512 steps)
Output: CSV files with Syn, Red, and ranks

Usage:
    python utils/compute_syn_red_rank.py
"""

import os
import pandas as pd
import numpy as np
from itertools import combinations
from tqdm import tqdm
from multiprocessing import Pool, cpu_count
import warnings

warnings.filterwarnings("ignore")

# ============================================================
# Configuration
# ============================================================
# 输入输出目录（可通过环境变量覆盖）
INPUT_DIR = os.environ.get("INPUT_DIR", "/root/data1/zjj/Neurlps2026/Results/Llama-3.1-8B-Instruct/data/L2_Norm")
OUTPUT_DIR = os.environ.get("OUTPUT_DIR", "/root/data1/zjj/Neurlps2026/Results/Llama-3.1-8B-Instruct/data/L2_Norm")

# File configurations
FILES_CONFIG = [
    {"name": "gsm8k_al", "type": "al", "per_head": True},
    {"name": "gsm8k_ml", "type": "ml", "per_head": False},
    {"name": "gsm8k_al_plus_ml", "type": "al_plus_ml", "per_head": False},
]

N_WORKERS = 16
USE_FAST_APPROXIMATION = False  # Use full PhiID calculation
MAX_QUESTIONS = 5  # Limit number of questions for quick testing (None for all)

# Path to effective lengths CSV (generated by 3_proxy_collection.py)
EFFECTIVE_LENGTHS_PATH = os.path.join(
    os.environ.get("OUTPUT_DIR", "/root/data1/zjj/Neurlps2026/Results/Llama-3.1-8B-Instruct/data/L2_Norm"),
    "gsm8k_effective_lengths.csv"
)

